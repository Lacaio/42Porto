#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t				i;
	const unsigned char	*c1;
	const unsigned char	*c2;

	c1 = (const unsigned char *)s1;
	c2 = (const unsigned char *)s2;
	i = 0;
	if (n == 0)
		return (0);
	while (c1[i] && (c1[i] == c2[i]) && i < n - 1)
		i++;
	return (c1[i] - c2[i]);
}
